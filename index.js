//root component

class App extends React.Component {
       
       constructor(props) {
           super(props);

           this.state = {
              power: true,
              bank: bankOne,
              volume:0.5,
              display: "Press the keys or click the pad"
            };
 
            this.handlePower = this.handlePower.bind(this);
            this.handleBankOne = this.handleBankOne.bind(this);
            this.handleBankTwo = this.handleBankTwo.bind(this);
            this.handleVolume = this.handleVolume.bind(this);
            this.handleDisplay = this.handleDisplay.bind(this);

       };

       handleDisplay(display) {
         this.setState({
              display:display
         })
       };

       handlePower() {
            this.setState((state) => {
                return {
                     power: !state.power,
                };
            });

            this.handleDisplay(`Power: ${this.state.power ? "ON" : "OFF"}`)
       };

       handleBankOne() {
           this.setState({
              bank: bankOne
           })
           this.handleDisplay("Bank: Bank One")
       };

       handleBankTwo() {
           this.setState({
              bank: bankTwo
           });
           this.handleDisplay("Bank: Bank Two");
       };

       handleVolume(e) {
           this.setState({
                volume:e.target.value
           });
           this.handleDisplay(`Volume: ${Math.floor((this.state.volume * 100))}`)
       };

         
    
        

     render() {

       let drumpad = []
       for(let i = 0; i < 9; i++) {
          drumpad.push(<DrumPad band={this.state.bank[i]} power={this.state.power} volume={this.state.volume} handleDisplay={this.handleDisplay} />)
       }


           return(
                <div id= "drum-machine" className="app-wrapper">
                  <div className="app-container">
                    
                    <div className="function-wrapper">
                     
                      <Display display={this.state.display}/>
                      <VolumeSlider handleVolume={this.handleVolume} volume={this.state.volume}/>
                     
                      <div className="sound-control">
                      <PowerPad power={this.state.power} handlePower={this.handlePower}/>
                      <BankPad handleBankOne = {this.handleBankOne} handleBankTwo = {this.handleBankTwo}/>
                      </div>
                    </div>
                   
                    <div className="pad-wrapper">
                     {
                      drumpad
                     }
                     
                    </div>

                    </div>
               </div>
           );
     };
};

